export function getBattleName(language: string, battle: Battle) {
  if (language === "fr") {
    return frenchBattleNames[battle.code];
  }
  return battle.name;
}

export function sanitizeBattleName(battleName: string): string {
  return battleName
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[- '()]/g, "")
    .toLowerCase();
}
